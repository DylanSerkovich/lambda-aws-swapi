        this.consumibles = consumibles;
        this.costos_en_creditos = costos_en_creditos;
        this.creado = creado;
        this.equipo = equipo;
        this.editado = editado;
        this.tamano = tamano;
        this.fabricante = fabricante;
        this.velocidad_maxima_atmosfera = velocidad_maxima_atmosfera;
